import httpx, json, sys

BASE = "http://127.0.0.1:8000/api/v1/agentapp"

def post(path, data):
    r = httpx.post(f"{BASE}{path}", json=data, timeout=10)
    j = r.json()
    if j.get("code") != 200:
        print(f"  FAIL {path}: {j.get('message')}")
        return None
    print(f"  OK {path}")
    return j["data"]

def put(path, data):
    r = httpx.put(f"{BASE}{path}", json=data, timeout=10)
    j = r.json()
    if j.get("code") != 200:
        print(f"  FAIL {path}: {j.get('message')}")
    else:
        print(f"  OK {path}")

